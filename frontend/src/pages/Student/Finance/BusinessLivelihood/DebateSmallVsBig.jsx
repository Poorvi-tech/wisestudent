import React, { useState } from "react";
import { useLocation } from "react-router-dom";
import GameShell from "../GameShell";
import useGameFeedback from "../../../../hooks/useGameFeedback";
import { getGameDataById } from "../../../../utils/getGameData";

const STAGES = [
  {
    id: 1,
    prompt: "A chai-wala earns ₹800/day informally. A registered café owner nearby earns ₹2,000/day but pays rent, tax, and staff. Who is better off long-term?",
    options: [
      { id: "opt1", text: "The chai-wala — lower costs mean higher real profit", outcome: "Incorrect. Without growth potential, ₹800/day becomes a ceiling, not a floor.", isCorrect: false },
      { id: "opt2", text: "The café owner — formal systems allow scaling, loans, and multiple outlets", outcome: "Correct! Short-term costs fund long-term compounding growth.", isCorrect: true },
      { id: "opt3", text: "Both are equal — money is money", outcome: "Incorrect. The café owner can multiply revenue; the chai-wala is capped.", isCorrect: false },
    ],
  },
  {
    id: 2,
    prompt: "An informal shop owner avoids registration to save ₹2,000/year in fees. What hidden costs is he paying instead?",
    options: [
      { id: "opt1", text: "None — saving money is always smart", outcome: "Incorrect. The invisible costs far exceed ₹2,000.", isCorrect: false },
      { id: "opt3", text: "He just pays a little extra in bribes, which is fine", outcome: "Incorrect. Bribes are illegal, unpredictable, and create vulnerability.", isCorrect: false },
      { id: "opt2", text: "No access to bank loans, government schemes, bulk supplier discounts, or legal protection", outcome: "Correct! The ₹2,000 saved costs lakhs in lost opportunities every year.", isCorrect: true },
    ],
  },
  {
    id: 3,
    prompt: "A small kirana store owner says: 'If I grow bigger, I'll have more stress and problems.' Is this true?",
    options: [
      { id: "opt1", text: "Yes — bigger always means more problems", outcome: "Incorrect. Bigger with systems means fewer daily fires, not more.", isCorrect: false },
      { id: "opt2", text: "Growth with proper systems (records, staff, digital tools) actually reduces daily stress", outcome: "Correct! Formal systems automate and delegate, reducing owner burden.", isCorrect: true },
      { id: "opt3", text: "He should stay small forever to avoid any risk", outcome: "Incorrect. Staying small carries its own risks: no savings, no insurance, no safety net.", isCorrect: false },
    ],
  },
  {
    id: 4,
    prompt: "When does staying small and informal actually make sense?",
    options: [
      { id: "opt1", text: "When the business is seasonal, part-time, or a side hustle with no growth ambition", outcome: "Correct! Not every venture needs to scale — but the choice should be conscious, not default.", isCorrect: true },
      { id: "opt2", text: "Always — small is beautiful", outcome: "Incorrect. Small by choice is fine; small by ignorance is a trap.", isCorrect: false },
      { id: "opt3", text: "When you want to avoid paying any taxes at all", outcome: "Incorrect. Tax avoidance is illegal and blocks all formal growth paths.", isCorrect: false },
    ],
  },
  {
    id: 5,
    prompt: "Final Verdict: Is it better to stay small and informal or grow big and formal?",
    options: [
      { id: "opt1", text: "Formalise early to unlock growth; stay small only if it's a conscious, informed choice", outcome: "Correct! The key is choosing your path with full knowledge of trade-offs.", isCorrect: true },
      { id: "opt2", text: "Always stay small — big businesses are only for rich people", outcome: "Incorrect. Many large businesses started as tiny informal setups that chose to formalise.", isCorrect: false },
      { id: "opt3", text: "Grow as fast as possible regardless of systems or planning", outcome: "Incorrect. Unplanned growth without systems leads to chaos and collapse.", isCorrect: false },
    ],
  },
];

const DebateSmallVsBig = () => {
  const location = useLocation();
  const totalStages = STAGES.length;
  const [currentStageIndex, setCurrentStageIndex] = useState(0);
  const [selectedChoice, setSelectedChoice] = useState(null);
  const [showResult, setShowResult] = useState(false);
  const [score, setScore] = useState(0);
  const { showAnswerConfetti, showCorrectAnswerFeedback, flashPoints } = useGameFeedback();

  // Registering at index 88
  const gameId = "finance-business-livelihood-finance-88";
  const gameData = getGameDataById(gameId);
  const totalCoins = gameData?.coins ?? location.state?.totalCoins ?? 10;
  const coinsPerLevel = Math.max(2, Math.floor(totalCoins / totalStages));
  const totalXp = gameData?.xp ?? location.state?.totalXp ?? 20;
  const stage = STAGES[currentStageIndex];

  const handleChoice = (option) => {
    if (selectedChoice || !stage) return;
    setSelectedChoice(option);
    if (option.isCorrect) {
      setScore((s) => s + 1);
      showCorrectAnswerFeedback(1, true);
    }
    setTimeout(() => {
      if (currentStageIndex === totalStages - 1) {
        setShowResult(true);
      } else {
        setCurrentStageIndex((i) => i + 1);
      }
      setSelectedChoice(null);
    }, 3500);
  };

  const progressLabel = `${currentStageIndex + 1}/${totalStages}`;

  return (
    <GameShell
      title="Debate: Small vs Big"
      subtitle={
        showResult
          ? "Debate concluded! Formalisation is the bridge from survival to success."
          : `Point ${currentStageIndex + 1} of ${totalStages}`
      }
      currentLevel={currentStageIndex + 1}
      totalLevels={totalStages}
      coinsPerLevel={coinsPerLevel}
      totalCoins={totalCoins}
      totalXp={totalXp}
      showGameOver={showResult}
      score={score}
      showConfetti={showResult && score === totalStages}
      flashPoints={flashPoints}
      showAnswerConfetti={showAnswerConfetti}
      gameId={gameId}
      gameType="finance"
      nextGamePath={location.state?.nextGamePath}
      nextGameId={location.state?.nextGameId}
      backPath={location.state?.returnPath}
    >
      <div className="space-y-8 max-w-4xl mx-auto">
        {!showResult && stage && (
          <div className="space-y-6">
            <div className="bg-slate-900/60 backdrop-blur-xl rounded-3xl p-6 md:p-10 border border-slate-700 shadow-2xl relative overflow-hidden">
              
              {/* Podium aesthetic */}
              <div className="absolute top-0 left-0 w-full h-2 bg-gradient-to-r from-cyan-500 to-sky-600"></div>

              <div className="flex items-center justify-between text-sm font-semibold uppercase tracking-[0.2em] text-slate-400 mb-8 border-b border-slate-700 pb-4">
                <span>Phase {progressLabel}</span>
                <span className="flex items-center gap-2">
                  <span className="w-2 h-2 rounded-full bg-emerald-400 animate-pulse"></span>
                  Score: {score}/{totalStages}
                </span>
              </div>
              
              <div className="text-center mb-10">
                <span className="inline-block py-1 px-3 rounded-full bg-cyan-900/50 text-cyan-300 text-xs font-bold uppercase tracking-wider mb-4 border border-cyan-500/30">
                  Topic of Debate
                </span>
                <p className="text-white text-xl md:text-2xl font-bold leading-relaxed">
                  "{stage.prompt}"
                </p>
              </div>

              <div className="grid grid-cols-1 gap-4">
                {stage.options.map((option) => {
                  const isSelected = selectedChoice?.id === option.id;
                  
                  let baseStyle = "from-slate-800 to-slate-900 border-slate-700 hover:border-cyan-500 hover:from-slate-800 hover:to-cyan-900/40 text-slate-200";
                  
                  if (isSelected) {
                    baseStyle = option.isCorrect
                      ? "from-emerald-900/80 to-emerald-800 border-emerald-500 text-emerald-100 shadow-[0_0_20px_rgba(16,185,129,0.3)] scale-[1.02]"
                      : "from-rose-900/80 to-rose-800 border-rose-500 text-rose-100 shadow-[0_0_20px_rgba(244,63,94,0.3)] scale-[1.02]";
                  } else if (selectedChoice && option.isCorrect && !isSelected) {
                    baseStyle = "from-emerald-900/30 to-slate-900 border-emerald-500/50 text-emerald-400/80 ring-1 ring-emerald-500/30 opacity-80";
                  } else if (selectedChoice) {
                    baseStyle = "from-slate-900 to-slate-900 border-slate-800 text-slate-600 opacity-40";
                  }

                  return (
                    <button
                      key={option.id}
                      onClick={() => handleChoice(option)}
                      disabled={Boolean(selectedChoice)}
                      className={`relative rounded-xl bg-gradient-to-r ${baseStyle} border-2 p-5 text-left font-medium transition-all duration-300 disabled:cursor-not-allowed`}
                    >
                      <div className="flex items-start gap-4">
                        <div className={`mt-0.5 flex-shrink-0 w-6 h-6 rounded-full border-2 flex items-center justify-center ${isSelected ? (option.isCorrect ? 'border-emerald-400 bg-emerald-500/20' : 'border-rose-400 bg-rose-500/20') : 'border-slate-600'}`}>
                           {isSelected && <div className={`w-2.5 h-2.5 rounded-full ${option.isCorrect ? 'bg-emerald-400' : 'bg-rose-400'}`}></div>}
                        </div>
                        <div className="flex-1">
                          <span className="block text-lg">{option.text}</span>
                          
                          <div className={`overflow-hidden transition-all duration-500 ${isSelected ? 'max-h-24 mt-3 opacity-100' : 'max-h-0 opacity-0'}`}>
                            <div className={`text-sm font-semibold p-3 rounded-lg ${option.isCorrect ? 'bg-emerald-500/20 text-emerald-300' : 'bg-rose-500/20 text-rose-300'}`}>
                              <span className="uppercase text-xs tracking-wider opacity-70 block mb-1">
                                {option.isCorrect ? 'Strong Argument' : 'Weak Argument'}
                              </span>
                              {option.outcome}
                            </div>
                          </div>
                        </div>
                      </div>
                    </button>
                  );
                })}
              </div>
            </div>
          </div>
        )}
      </div>
    </GameShell>
  );
};

export default DebateSmallVsBig;
